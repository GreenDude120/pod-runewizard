		<div id="JsHelpBody" style="display: block;">
<h3>What is it?</h3>
The Runewizard lets you check easily what runewords you can make with the runes you have.<br>
Click on the runes to indicate which ones you have found. The available runewords will be highlighted automatically.<br>
The table can be sorted, try clicking on "Type" to sort runewords by equipment type, you may find it useful!<br>
<br>
<h3>Runes</h3>
The runes are listed in order of rarity, from the most common to the most rare, from top to bottom, and left to right.
The vertical groups of runes represent roughly <strong>Common</strong>, <strong>Semi-Rare</strong>, and <strong>Extremely Rare</strong> runes. Note also that the order is
consistent with the rune <a href="http://www.battle.net/diablo2exp/items/cube.shtml">upgrade formulas for the Horadric Cube</a>. For example 3 x Tal = 1 x Ral rune.<br>
